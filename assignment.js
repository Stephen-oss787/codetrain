// initialize a counter variable. let counter = 0;
let count = 0;

// function to increment the count by 1
function increment () {
  count += 1;
  console.log(count);
}
increment()

// funtion to decrement the count by 1
function decrement() {
  count -= 1;
  console.log(count);

}
decrement()

// function to display the current count in the console
function displayCount() {
  console.log(count);

}
displayCount()